        "        if self.epsilon > np.random.rand():\n",
        "            # explore\n",
        "            return np.random.choice(ACTION_SPACE)\n",
        "        else:\n",
        "            # exploit\n",
        "            state = self._reshape_state_for_net(state)\n",
        "            q_values = self.online_network.predict(state)[0]\n",
        "            return np.argmax(q_values)\n",
